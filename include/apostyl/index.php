<li><span class="light">- "подвійний апостиль"</span> (Португалія, Бельгія, Австрія, Нідерланди, Швейцарія, Великобританія);</li>
<li><span class="light">- апостиль + консульська легалізація</span> (Франція, Італія, Іспанія).</li>
</ul>
