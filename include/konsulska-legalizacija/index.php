<div class="ho-show down-ar"><p>Що таке консульська легалізація?</a></div>

<div class="showed-content">
<p><strong>Консульська легалізація</strong> - це один зі способів легалізації документів.   Аналогічний апостилю. На відміну від апостилю застосовується в документообігу з організаціями, установами, та державними органами влади країн, що не є підписантами Гаазької конвенції про відміну вимог легалізації офіційних документів 1961 року. Порівняно з апостилем є більш складною багатоетапною процедурою.</p>
