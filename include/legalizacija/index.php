<p><span class="light">Легалізація документів</span> – це офіційна процедура надання юридичної сили документам, що видані в одній країні,  для їх використання в іншій.
Сутність легалізації  полягає в засвідченні справжності підпису посадової особи на офіційному документі, а також печатки уповноваженого державного органу.</p>
<p><span class="light">Легалізації підлягають</span> складені відповідними державними органами документи, необхідні  для використання за кордоном , якщо інше не передбачено положеннями міжнародних договорів,  які скасовують  процедуру легалізації і учасниками яких є Україна та держава, на території якої будуть використовуватися ці документи.</p>

</div>
