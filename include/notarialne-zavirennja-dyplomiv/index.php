                        <li>Одночасне засвідчення нотаріусом вірності фотокопії (копії) документа та підпису перекладача. В цьому випадку переклад пришивається до копії документа. При цьому нотаріус офіційно засвідчує відповідність копії оригіналу та ставить на ньому штамп «копія» або «фотокопія».</li></ul></p>

                        <p><strong><span class="light">Нотаріально засвідчити</span> </strong> переклад можна тільки тих офіційних документів, які складені відповідно до вимог діловодства:
