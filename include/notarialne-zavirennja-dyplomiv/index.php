                <p><strong><span class="light">Нотаріальний переклад</span> </strong> (нотаріально засвідчений переклад) — переклад документа, виконаний дипломованим перекладачем та засвідчений нотаріусом.</p>
                <p>Офіційно засвідчити переклад можна двома способами:
                    <ul>
